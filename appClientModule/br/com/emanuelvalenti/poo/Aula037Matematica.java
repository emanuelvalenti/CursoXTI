package br.com.emanuelvalenti.poo;

public class Aula037Matematica {

	/**
	 * 
	 * @param um
	 * @param dois
	 * @return o maior de dois nÃºmeros
	 */
	public int maior(int um, int dois) {

		if (um >= dois) {
			return um;
		} else {
			return dois;
		}

	}

	/**
	 * 
	 * @param um
	 * @param dois
	 * @return
	 */
	public double soma(double um, double dois) {

		return um + dois;
		
	}
	
	/**
	 * 
	 * @param um
	 * @param dois
	 * @return
	 */
	public double divide(double um, double dois){

		return um / dois;
		
	}

}
